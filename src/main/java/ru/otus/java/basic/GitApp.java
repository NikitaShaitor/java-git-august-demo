package ru.otus.java.basic;

public class GitApp {
    public static void main(String[] args) {
        System.out.println("Калькулятор: Начало");

        System.out.println("Вычитание");
        int subtrackA = 10;
        int subtrackB = 20;
        int subtrackResult = subtrackB - subtrackA;

        System.out.println(subtrackB + " - " + subtrackA + " = " + subtrackResult);
    }
}
