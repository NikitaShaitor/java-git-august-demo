package ru.otus.java.basic;

public class GitApp {
    public static void main(String[] args) {
        System.out.println("Калькулятор: Начало");

        System.out.println("Задача суммирования");
        int sumA = 1;
        int sumB = 3;
        int sumResult = sumA + sumB;
        System.out.println(sumA + " + " + sumB + " = " + sumResult);

        System.out.println("Задача вычитания");
        int subtrackA = 10;
        int subtrackB = 12;
        int subtrackResult = subtrackB - subtrackA;

        System.out.println(subtrackB + " - " + subtrackA + " = " + subtrackResult);

        System.out.println("task-1");
    }
}
